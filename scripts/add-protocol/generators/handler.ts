/**
 * Handler Generator — Protocol Onboarding Pipeline
 *
 * Generates a ProtocolHandler class file following the t2-handlers.ts pattern.
 * Output: a self-contained TypeScript module that exports a handler class and
 * singleton instance for the target protocol.
 */

import type { AnnotationConfig, ParsedInstruction } from "../types.js";
import { pascalCase, camelCase } from "../naming.js";

export function generateHandler(
  config: AnnotationConfig,
  parsed: ParsedInstruction[],
): string {
  const { protocol } = config;
  const pascal = pascalCase(protocol.id);
  const camel = camelCase(protocol.id);
  const dispatchFnName = `dispatch${pascal}Compose`;
  const metadataConst = `${pascal.toUpperCase()}_METADATA`;
  const programConst = `${pascal.toUpperCase()}_PROGRAM`;

  // ── Build supportedActions Map entries ──────────────────────────────────

  const actionEntries = parsed.map((ix) => {
    return `    ["${ix.sdkName}", { actionType: ActionType.${ix.actionType}, isSpending: ${ix.isSpending} }],`;
  });

  // ── Build summarize switch cases ───────────────────────────────────────

  const summarizeCases = parsed.map((ix) => {
    return `      case "${ix.sdkName}": return \`${protocol.displayName} ${ix.sdkName} \${JSON.stringify(params)}\`;`;
  });

  // ── Assemble output ────────────────────────────────────────────────────

  return `/**
 * ${protocol.displayName} Handler — Kit-native (auto-generated)
 *
 * Generated by the protocol onboarding pipeline.
 * Implements ProtocolHandler for ${protocol.displayName}.
 *
 * Program: ${protocol.programAddress}
 * Actions: ${parsed.length}
 */

import type { Address } from "@solana/kit";
import type {
  ProtocolHandler,
  ProtocolHandlerMetadata,
  ProtocolComposeResult,
  ProtocolContext,
} from "./protocol-handler.js";
import { ActionType } from "../generated/types/actionType.js";
import { ${dispatchFnName} } from "./${protocol.id}-compose.js";

// ─── Program ID ──────────────────────────────────────────────────────────────

const ${programConst}: Address = "${protocol.programAddress}" as Address;

// ─── Metadata ────────────────────────────────────────────────────────────────

const ${metadataConst}: ProtocolHandlerMetadata = {
  protocolId: "${protocol.id}",
  displayName: "${protocol.displayName}",
  programIds: [${programConst}],
  supportedActions: new Map([
${actionEntries.join("\n")}
  ]),
};

// ─── Handler ─────────────────────────────────────────────────────────────────

export class ${pascal}Handler implements ProtocolHandler {
  readonly metadata = ${metadataConst};

  async compose(
    ctx: ProtocolContext,
    action: string,
    params: Record<string, unknown>,
  ): Promise<ProtocolComposeResult> {
    return ${dispatchFnName}(ctx, action, params);
  }

  summarize(action: string, params: Record<string, unknown>): string {
    switch (action) {
${summarizeCases.join("\n")}
      default: return \`${protocol.displayName}: \${action}\`;
    }
  }
}

// ─── Singleton ───────────────────────────────────────────────────────────────

export const ${camel}Handler = new ${pascal}Handler();
`;
}
